function extractBezierVertices(input_function) {
  // convert the function to a string
  let function_str = input_function.toString()
  // create a list to hold the vertices
  let bezierVertices = []

  // find all the vertex and bezierVertex calls
  let matches = function_str.match(
    /(vertex|bezierVertex)\(([-?\d+\.?\d*,\s?-?\d+\.?\d*]+)\)/g
  )

  // loop through the matches
  for (let i = 0; i < matches.length; i++) {
    let match = matches[i]
    // split the matched string on commas, convert the strings to floats, and add them to the list of vertices
    bezierVertices.push(
      match
        .replace(/(vertex|bezierVertex)\(/g, '')
        .replace(/\)/g, '')
        .split(',')
        .map(function (coord) {
          return parseFloat(coord)
        })
    )
  }
  return bezierVertices
}

function extractVertexPoints(vertices) {
  let VertexArray = []
  for (let arr of vertices) {
    if (arr.length == 2) {
      VertexArray.push(arr)
    } else if (arr.length > 2) {
      VertexArray.push(arr.slice(-2))
    }
  }
  return VertexArray
}

function scaleVertexArrays(vertices, scale) {
  let scaledVertices = []
  for (let arr of vertices) {
    scaledVertices.push(arr.map((coord) => coord * scale))
  }
  return scaledVertices
}

function offsetVertexArrays(scaledArray, offsetX, offsetY) {
  let offsetVertices = []
  for (let arr of scaledArray) {
    offsetVertices.push([arr[0] + offsetX, arr[1] + offsetY])
  }
  return offsetVertices
}

function getObjectArrays(text_object, scale, offsetX, offsetY) {
  // add bezier vertices to text objects.
  text_object.bezierVertices = extractBezierVertices(text_object.shape)
  // add vertex points arrays to text objects.
  text_object.vertexPoints = extractVertexPoints(text_object.bezierVertices)
  // add scaled bezier vertices array to text objects.
  text_object.scaledBezierVertices = scaleVertexArrays(
    text_object.bezierVertices,
    scale
  )
  // add scaled vertex points arrays to text objects
  text_object.scaledVertexPoints = scaleVertexArrays(
    text_object.vertexPoints,
    scale
  )
  //offset vertex points for boids.
  text_object.offsetVertices = offsetVertexArrays(
    text_object.scaledVertexPoints,
    offsetX,
    offsetY
  )
}

function getAllObjectArrays(objectArray, scale, offsetX, offsetY) {
  for (let object of objectArray) {
    getObjectArrays(object, scale, offsetX, offsetY)
  }
}
